<p><strong>Post Type</strong><br />
Decide if the content will be presented as a post or page format. We recommend to use 'Post' as a type.</p>

<p><strong>Post Status </strong><br />
Selecting the status of the post allows you to choose how to manage the content once it is retrieved. Posts with status 'Publish' will be published automatically. To review the posts before they are featured choose one of the following statuses: future, pending or draft. </p>

<p><strong>Tagging Taxonomy</strong><br />
Most of 90min's posts are tagged under one or more of the following categories: World News, Lists, Tv, Transfer, Rumors and Top News. This field indicates the appearance format of those values: tags or categories. We recommend choosing 'Category'</p>

<p><strong>Tags</strong><br />
90min posts contain team, league and player tags related to the article content . Checking 'Provide 90min's post specific tags' will include these tags in the created article.</p>

<p><strong>Author</strong><br />
Within 90min's post you have the option to include or remove the author's name.</p>

<p><strong>SEO</strong><br />
Checking 'Don't index posts created by the 90min plugin ' will disable search engines from crawling 90min posts.<br  />
<img src="<?=plugin_dir_url( __FILE__ )."images/seo-index.png"?>" /></p>

<p><strong>Language & Feed Selection</strong><br  />
90min allows you to retrieve our syndicated content library in its entirety for each of our supported languages. You can choose to retrieve the content feed by league, team or a specific category such as world news, lists, TV and transfers. Not all content is available for all languages. Select your language first.</p>

